public class Fabian_Operaciones {

    public Fabian_Operaciones () {
    }

    public float FabianSuma(final float fabian_Num1, final float fabian_Num2){
        return(fabian_Num1+fabian_Num2);
    }

    public float FabianRestar(final float fabian_Num1, final float fabian_Num2){
        return(fabian_Num1-fabian_Num2);
    }

    public float FabianMultiplicar(final float fabian_Num1, final float fabian_Num2){
       return(fabian_Num1*fabian_Num2);
    }

    public float FabianDividir(final float fabian_Num1, final float fabian_Num2){
       return(fabian_Num1/fabian_Num2);
    }

}

